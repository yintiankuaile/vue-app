<template>
  <section>
    <header class="top_tips">
      <span class="num_tip" v-if="fatherComponent == 'tool'">{{level}}</span>
      <span class="num_tip" v-if="fatherComponent == 'item'">题目{{itemNums}}</span>
    </header>
    <div v-if="fatherComponent == 'tool'">
      <div class="home_logo item_container_style"></div>
      <router-link to="item" class="start button_style"></router-link>
    </div>
    <div v-if="fatherComponent == 'item'">
      <div class="item_back item_container_style">
        <div class="item_list_container" v-if="itemDetail.length > 0">
          <header class="item_title">{{itemDetail[itemNums-1].topic_name}}</header>
          <ul>
            <li v-for="(item, index) in itemDetail[itemNums-1].topic_answer" @click="choosed(index, item.topic_answer_id)" class="item_list">
              <span class="option_style" v-bind:class="{'has_choosed':choosedNum == index}">{{chooseType(index)}}</span>
              <span class="option_detail">{{item.answer_name}}</span>
            </li>
          </ul>
        </div>
      </div>
      <span class="next_item button_style" @click="nextItem" v-if="itemNums < itemDetail.length"></span>
      <span class="submit_item button_style" v-else @click="submitAnswer"></span>
    </div>
  </section>
</template>

<script>
  import { mapState, mapActions } from 'vuex'
  export default {
    name: 'itemcontainer',
    data(){
      return {
        choosedNum: null, //选中答案的索引
        choosedId: null //选中答案的id
      }
    },
    props:['fatherComponent'],
    computed: mapState([
      'level', //第几周
      'itemNums', //第几题
      'itemDetail' //题目、选项
    ]),
    methods: {
      ...mapActions([
        'addChoosedNum',
        'initializeData'
      ]),
      //存储选中的选项id
      choosed(type, id){
        this.choosedNum = type;
        this.choosedId = id;
      },
      //根据索引把选项索引变成A、B、C、D
      chooseType: type => {
          switch(type){
            case 0 : return 'A';
            case 1 : return 'B';
            case 2 : return 'C';
            case 3 : return 'D';
          }
      },
      //点击下一步
      nextItem(){
        if (this.choosedNum != null){
          this.choosedNum = null;
          //触发保存答案事件
          this.addChoosedNum(this.choosedId);
        }else {
            alert("你还没有选择选项");
        }
      },
      //最后一步，全部完成，提交
      submitAnswer(){
        if (this.choosedNum != null){
          this.choosedNum = null;
          //触发保存答案事件
          this.addChoosedNum(this.choosedId);
          //题目全部答完，跳转页面
          this.$router.push('mine');
        }else {
          alert("你还没有选择选项");
        }
      }
    },
    created(){
      if (this.$route != 'item'){
        this.initializeData();
      }
    }
  }
</script>

